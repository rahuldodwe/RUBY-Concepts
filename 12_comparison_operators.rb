# Comparison Operators

=begin 
  Comparison Operators: 
  > greater than
  < less than
  >= greater than or equal to
  <= less than or equal to
  == equal to
  != not equal to
=end

a = 100
b = 10
c = 10

puts a > b #true
puts a < b #false
puts a >= c #true
puts a <= b #false
puts a == c #true
